<p>La conscience de soi ne peut pas nous offrir une image complète de notre être. La plupart des phénomènes psychiques (sentiments, pensées, désirs, rêves, peurs, perceptions) ont lieu sans qu'on s'en rende compte ou ont pour origine des phénomènes inconscients.</p>

